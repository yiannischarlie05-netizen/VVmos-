"""
DeviceBackdater — Forensic Metadata Alignment Engine

Enforces strict forensic metadata alignment across the entire device filesystem
after Genesis Pipeline injection. Without this, injected artifacts carry "just
created" filesystem timestamps that forensic scanners and anti-fraud engines
detect as synthetic injection indicators.

Responsibilities:
  1. Filesystem mtime/ctime alignment — backdate directories and files to match
     the stochastic timeline generated by StochasticAgingEngine's Poisson models
  2. EXIF metadata alignment — ensure gallery photo EXIF DateTimeOriginal headers
     match both their file mtime and the overall device age timeline
  3. UsageStats XML backdating — generate/backdate the /data/system/usagestats/0/
     daily XML files that Android's UsageStatsService queries to determine app
     usage patterns over time
  4. App install time alignment — backdate APK directories + call pm set-install-time
     to update PackageManager's internal database (Settings > Apps shows correct date)
  5. SQLite WAL journal cleanup — remove -wal/-shm journals whose creation times
     leak the actual injection timestamp
  6. Media scanner trigger — broadcast ACTION_MEDIA_SCANNER_SCAN_FILE to re-index
     EXIF-dated gallery photos so they appear organically in the gallery app
  7. Inode entropy — vary timestamps across files to avoid the "all inodes in same
     second" forensic anomaly that reveals batch injection

Architecture:
  - Standalone module importable by ProfileInjector, VMOSGenesisV3, UnifiedGenesisEngine
  - Supports both local ADB (Cuttlefish) and VMOS Cloud (async_adb_cmd) targets
  - Accepts optional StochasticAgingEngine output for timestamp coherence
  - Non-destructive: only modifies timestamps, never file content

Limitations:
  - ctime (inode change time) is set by the kernel on metadata operations — we can
    only approximate it by performing the touch operation at a controlled time
  - packages.xml firstInstallTime is ABX2 binary format — cannot be directly modified
  - /proc timestamps are kernel-generated and immutable
"""

import logging
import os
import random
import time
from dataclasses import dataclass, field
from datetime import datetime, timedelta
from typing import Any, Dict, List, Optional, Tuple

logger = logging.getLogger(__name__)

# ── ADB helpers (import from adb_utils if available, fallback to subprocess) ──

try:
    from adb_utils import adb_shell as _adb_shell_raw, adb_with_retry as _adb_retry
    _HAS_ADB_UTILS = True
except ImportError:
    _HAS_ADB_UTILS = False

import subprocess


def _adb_shell(target: str, cmd: str, timeout: int = 30) -> str:
    """Execute shell command on device via ADB."""
    if _HAS_ADB_UTILS:
        try:
            return _adb_shell_raw(target, cmd)
        except Exception:
            pass
    try:
        full = f"adb -s {target} shell \"{cmd}\""
        result = subprocess.run(
            full, shell=True, capture_output=True, text=True, timeout=timeout
        )
        return result.stdout.strip()
    except Exception as e:
        logger.debug(f"ADB shell failed: {e}")
        return ""


# ── Result data structures ──────────────────────────────────────────────────

@dataclass
class BackdateResult:
    """Result of a full device backdating operation."""
    success: bool = False
    directories_backdated: int = 0
    files_backdated: int = 0
    photos_exif_aligned: int = 0
    usage_stats_days: int = 0
    apps_install_dated: int = 0
    wal_journals_cleaned: int = 0
    media_scanner_triggered: bool = False
    errors: List[str] = field(default_factory=list)
    elapsed_seconds: float = 0.0

    def to_dict(self) -> Dict[str, Any]:
        return {
            "success": self.success,
            "directories_backdated": self.directories_backdated,
            "files_backdated": self.files_backdated,
            "photos_exif_aligned": self.photos_exif_aligned,
            "usage_stats_days": self.usage_stats_days,
            "apps_install_dated": self.apps_install_dated,
            "wal_journals_cleaned": self.wal_journals_cleaned,
            "media_scanner_triggered": self.media_scanner_triggered,
            "errors": self.errors,
            "elapsed_seconds": round(self.elapsed_seconds, 2),
        }


