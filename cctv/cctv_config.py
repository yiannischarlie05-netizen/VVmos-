def get_cidrs_for_countries(countries: list[str]) -> list[str]:
    """Get combined CIDR blocks for a list of countries."""
    result = []
    for c in countries:
        if c in GLOBAL_CIDRS:
            result.extend(GLOBAL_CIDRS[c])
    return result

def get_cidrs_for_region(region: str) -> list[str]:
    """Get combined CIDRs for a region preset."""
    countries = REGIONS.get(region, [])
    return get_cidrs_for_countries(countries)

def list_available_countries() -> list[str]:
    return sorted(GLOBAL_CIDRS.keys())

def list_available_regions() -> list[str]:
    return sorted(REGIONS.keys())
